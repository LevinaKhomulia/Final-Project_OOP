create table Barang(
Kode varchar(6),
Nama varchar(50),
Keterangan varchar(50),
Harga money,
Satuan varchar(50),
Stok varchar(50)
constraint pk_Barang primary key (Kode)
);

create table Supplier(
Kode varchar(6),
Nama varchar(50),
Kota varchar(50),
constraint pk_Supplier primary key (Kode)
);

create table BarangSupplier(
KodeBarang varchar(6),
KodeSupplier varchar(6),
HargaSupplier money,
constraint pk_BarangSupplier primary key (KodeBarang,KodeSupplier),
constraint fk_BarangSupplier_Barang foreign key (KodeBarang) references Barang(Kode) on delete cascade on update cascade,
constraint fk_BarangSUpplier_Supplier foreign key (KodeSupplier) references Supplier(Kode) on delete cascade on update cascade
);

